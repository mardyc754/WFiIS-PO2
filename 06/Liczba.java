/**
 * Klasa abstrakcyjna opisujaca liczby wymierne
 */
public abstract class Liczba {
    /**
     * Metoda zwracajaca wartosc liczby
     * @return Wartosc typu double reprezentowana przez obiekt
     */
    public abstract double toDouble();

    /**
     * Metoda zwracajaca string przedstawiajacy liczbe
     * @return Reprezentacja liczby w postaci stringu
     */
    public abstract String toString();
}
